#include "ft_printf.h"

int	ft_print_str(char *str)
{
	int	count;

	count = 0;
	if (!str)
		return (write(1, "(null)", 6));
	while (*str)
	{
		if (write(1, str, 1) == -1)
			return (-1);
		count++;
		str++;
	}
	return (count);
}
